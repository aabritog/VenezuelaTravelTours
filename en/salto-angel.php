		    		<br><br>
		    		Angel Falls trips take place from June to December , when the rivers are strong enough to withstand the wooden canoes ( canoeing ) used by the indigenous Pemon ethnic depth. During the dry season ( December to March) there is less water flow than what is seen in some pictures. The elusive jump can not be seen on cloudy days , so visitors have no guarantee of being able to see it in its fullness .
